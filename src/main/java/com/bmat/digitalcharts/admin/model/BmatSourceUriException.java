package com.bmat.digitalcharts.admin.model;

public class BmatSourceUriException extends Exception {

	private static final long serialVersionUID = 1197876307957782875L;

	public BmatSourceUriException() {
	}

	public BmatSourceUriException(String message) {
		super(message);
	}

	public BmatSourceUriException(Throwable cause) {
		super(cause);
	}

	public BmatSourceUriException(String message, Throwable cause) {
		super(message, cause);
	}

	public BmatSourceUriException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
